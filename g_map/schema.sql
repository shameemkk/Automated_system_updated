create or replace function fetch_queries(
  p_batch_size int
)
returns setof client_queries
language plpgsql
as $$
begin
  return query
  update client_queries
  set status = 'processing'
  where id in (
    select id
    from client_queries
    where status = 'queued'
    order by client_tag, created_at
    limit p_batch_size
    for update skip locked
  )
  returning *;
end;
$$;

