-- Advisory-lock based atomic dedup insert for global and client_tag modes.
-- Acquires a per-website (or per-website+client_tag) transaction-level advisory lock,
-- checks both tables, and inserts only if the website is not already present.
-- This closes the race condition window without requiring a unique constraint.
CREATE OR REPLACE FUNCTION insert_results_deduped(
  p_results jsonb,
  p_dedup_mode text
)
RETURNS int
LANGUAGE plpgsql
AS $$
DECLARE
  v_row         jsonb;
  v_website     text;
  v_client      text;
  v_automation  bigint;
  v_lock        bigint;
  v_exists      boolean;
  v_count       int := 0;
BEGIN
  FOR v_row IN SELECT jsonb_array_elements(p_results)
  LOOP
    v_website    := v_row->>'website';
    v_client     := v_row->>'client_tag';
    v_automation := (v_row->>'automation_id')::bigint;

    -- Skip rows with no website — pg_try_advisory_xact_lock(NULL) would crash
    IF v_website IS NULL OR v_website = '' THEN
      CONTINUE;
    END IF;

    IF p_dedup_mode = 'global' THEN
      -- Try non-blocking lock per website (64-bit md5 hash).
      -- If lock unavailable, another worker is already handling this website — skip safely.
      v_lock := ('x' || substr(md5(v_website), 1, 16))::bit(64)::bigint;
      IF NOT pg_try_advisory_xact_lock(v_lock) THEN
        CONTINUE;
      END IF;

      SELECT EXISTS (
        SELECT 1 FROM client_query_results        WHERE website = v_website
        UNION ALL
        SELECT 1 FROM "google map scraped data v1" WHERE website = v_website
      ) INTO v_exists;

    ELSIF p_dedup_mode = 'client_tag' THEN
      -- Try non-blocking lock per (website + client_tag).
      -- If lock unavailable, another worker for this client is handling it — skip safely.
      v_lock := ('x' || substr(md5(v_website || '::' || v_client), 1, 16))::bit(64)::bigint;
      IF NOT pg_try_advisory_xact_lock(v_lock) THEN
        CONTINUE;
      END IF;

      SELECT EXISTS (
        SELECT 1 FROM client_query_results        WHERE website = v_website AND client_tag = v_client
        UNION ALL
        SELECT 1 FROM "google map scraped data v1" WHERE website = v_website AND client_tag = v_client
      ) INTO v_exists;

    ELSIF p_dedup_mode = 'no_need' THEN
      -- Try non-blocking lock per (website + automation_id).
      IF v_automation IS NULL THEN
        -- No automation_id to scope dedup against — just insert
        v_exists := false;
      ELSE
        v_lock := ('x' || substr(md5(v_website || '::' || v_automation::text), 1, 16))::bit(64)::bigint;
        IF NOT pg_try_advisory_xact_lock(v_lock) THEN
          CONTINUE;
        END IF;

        SELECT EXISTS (
          SELECT 1 FROM client_query_results WHERE website = v_website AND automation_id = v_automation
        ) INTO v_exists;
      END IF;

    ELSE
      v_exists := false;
    END IF;

    IF NOT v_exists THEN
      INSERT INTO client_query_results (
        client_query_id, client_tag, automation_id, name, website,
        types, zip_code, phone_number, full_address, city,
        place_link, timezone, review_count, rating, mode
      ) VALUES (
        (v_row->>'client_query_id')::bigint,
        v_client,
        v_automation,
        v_row->>'name',
        v_website,
        CASE
          WHEN v_row->'types' IS NOT NULL AND jsonb_typeof(v_row->'types') = 'array'
          THEN ARRAY(SELECT jsonb_array_elements_text(v_row->'types'))
          ELSE NULL
        END,
        v_row->>'zip_code',
        v_row->>'phone_number',
        v_row->>'full_address',
        v_row->>'city',
        v_row->>'place_link',
        v_row->>'timezone',
        (v_row->>'review_count')::int,
        (v_row->>'rating')::numeric,
        v_row->>'mode'
      );
      v_count := v_count + 1;
    END IF;
  END LOOP;

  RETURN v_count;
END;
$$;



